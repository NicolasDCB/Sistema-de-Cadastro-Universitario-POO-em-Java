    private void btnCadastrarFuncionariActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnCadastrarFuncionariActionPerformed
        String nome = jTextFieldNome.getText();
        String codigoFuncionario = jTextFieldCodigo.getText();
        String salarioStr = jTextFieldSalario.getText();
        String tipo = (String) jComboTipo.getSelectedItem();
        String nivel = (String) jComboNivel.getSelectedItem();
        String codigoDepartamento = (String) cmbDepartamento1.getSelectedItem();
        String area = jTextFieldArea.getText();
        String titulo = jTextFieldTitulo.getText();
        String cargaStr = jTextFieldCarga.getText();

        if (nome.isEmpty() || codigoFuncionario.isEmpty() || salarioStr.isEmpty() || codigoDepartamento == null) {
            JOptionPane.showMessageDialog(this, "Preencha todos os campos corretamente.");
            return;
        }

        double salario;
        try {
            salario = Double.parseDouble(salarioStr);
        } catch (NumberFormatException e) {
            JOptionPane.showMessageDialog(this, "Salário inválido.");
            return;
        }

        Funcionario f = null;
        switch (tipo) {
            case "Tecnico" -> {
                String funcao = jTextFieldFuncao.getText();
                if (funcao.isEmpty()) {
                    JOptionPane.showMessageDialog(this, "Informe a função do Técnico.");
                    return;
                }
                f = new Tecnico(salario, nome, codigoFuncionario, nivel, funcao);
            }
            case "Efetivo" -> {
                if (area.isEmpty()) {
                    JOptionPane.showMessageDialog(this, "Informe a área do Efetivo.");
                    return;
                }
                f = new Efetivo(salario, nome, codigoFuncionario, 0, titulo, nivel, area);
            }
            case "Substituto" -> {
                int cargaHoraria;
                try {
                    cargaHoraria = Integer.parseInt(cargaStr);
                } catch (NumberFormatException e) {
                    JOptionPane.showMessageDialog(this, "Carga horária inválida.");
                    return;
                }
                f = new Substituto(salario, nome, codigoFuncionario, titulo, nivel, cargaHoraria);
            }
        }

        if (f != null) {
            controlador.adicionarFuncionario(codigoDepartamento, f);
            JOptionPane.showMessageDialog(this, "Funcionário cadastrado com sucesso!");
            jTextFieldNome.setText("");
            jTextFieldCodigo.setText("");
            jTextFieldSalario.setText("");
            jTextFieldArea.setText("");
            jTextFieldCarga.setText("");
            jTextFieldTitulo.setText("");
        } else {
            JOptionPane.showMessageDialog(this, "Erro ao criar funcionário.");
        }
    }//GEN-LAST:event_btnCadastrarFuncionariActionPerformed

