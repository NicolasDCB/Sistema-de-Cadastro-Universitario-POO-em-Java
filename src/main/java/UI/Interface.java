    private void GeralActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_GeralActionPerformed
          String funcionarios = controlador.exibirTodosFuncionarios();
    String[] departamentos = controlador.resumoDepartamentos();

    if ((funcionarios == null || funcionarios.isEmpty()) && (departamentos == null || departamentos.length == 0)) {
        JOptionPane.showMessageDialog(this, "Nenhum dado encontrado.");
        return;
    }

    StringBuilder sb = new StringBuilder();
    sb.append("=== Funcionários ===\n").append(funcionarios).append("\n");
    sb.append("=== Departamentos ===\n");
    for (String d : departamentos) {
        sb.append(d).append("\n");
    }

    JOptionPane.showMessageDialog(this, sb.toString(), "Relatório Geral", JOptionPane.INFORMATION_MESSAGE);

    }//GEN-LAST:event_GeralActionPerformed

